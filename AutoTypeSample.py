# pyautogui is a amazing ibrary given by python to print some particular lines in your document itself
# Auto typing is the most trending feature.
# You may have noticed this in the whatsapp as well
# Using this library we can build an application where the text is written in the document itself

# importing the library 
import pyautogui as ac


# The main and beautiful feature used to typewrite the text.
ac.typewrite('Hello World\n')

# We can set the intervals as well
ac.typewrite('Hello World', interval=0.25)


